#include <iostream>

using namespace std;

int main(){
	
	// Variables
	int x;
	
	// 1.- Asignación básica
	x = 6;
	cout << "1.- x = " << x << endl;
	
	// 2.- Asignación suma
	x = 6;
	x += 4;
	cout << "2.- x = " << x << endl;
	
	// 3.- Asignación resta
	x = 6;
	x -= 1;
	cout << "3.- x = " << x << endl;
	
	// 4.- Asignación producto
	x = 6;
	x *= 5;
	cout << "4.- x = " << x << endl;
	
	// 5.- Asignación división
	x = 6;
	x /= 2;
	cout << "5.- x = " << x << endl;
	

	// 6.- Asignación modulo
	x = 6;
	x %= 4;
	cout << "7.- x = " << x << endl;
	
	return 0;
}
