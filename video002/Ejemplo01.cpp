// Variables

#include <iostream>

using namespace std;

int main(){

	// Variables
	int n1, n2, suma, producto;
	
	// Datos
	n1 = 18;
	n2 = 9;
	
	// Proceso
	suma = n1 + n2;
	producto = n1 * n2;
	
	// Reporte
	cout << "Número 1: " << n1 << endl;
	cout << "Número 2: " << n2 << endl;
	cout << "Suma: " << suma << endl;
	cout << "Producto: " << producto << endl;
	
	return 0;
}
